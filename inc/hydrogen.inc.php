            <article>
                <p>Перекись водорода является сильным окислителем со множеством применений. Перекись водорода используют для очистки промышленных сточных вод и бытовых сточных вод, также используют в качестве дезинфицирующего средства асептической упаковки.</p>
                <p>Тест-наборы <a href="/methods.php#chemets">CHEMets</a> и <a href="/methods.php#vacu-vials">Vacu-vials</a> широко используются в пищевой промышленности и при производстве напитков для контроля стерилизации растворов в процессе упаковки и стерилизации.</p>
                <h2>Метод тиоцианат железа</h2>
                <p>Метод основан на работе тиоцианат аммония и двухвалентного железа в кислой среде. Перекись водорода окисляет двухвалентное железо до трехвалентного, в результате образуется красный комплекс тиоцианата. Хлор не оказывает «помехи» на этот метод. Трехвалентное железо будет мешать. Результаты выражаются в ppm (мг/л) H<sub>2</sub>O<sub>2</sub>.</p>
                <h2>Метод DPD</h2>
                <p>В этом методе перекись водорода вступает в реакцию с DPD (N,N-диэтил-р- фенилендиамин) в присутствии йодида калия и молибдата аммония, в результате формируется розово-окрашенный комплекс. Результаты выражаются в ppm (мг/л) H<sub>2</sub>O<sub>2</sub>.</p>
                <h2>Титриметрический метод сульфата церия</h2>
                <p>CHEMetrics разработал титриметрический метод, использующий сульфат церия в качестве титранта и ферроин в качестве индикатора конечной точки. Изменение окрашивания с зеленого на оранжевый сигнализирует о конце титрования. Результаты выражаются в % H<sub>2</sub>O<sub>2</sub>.</p>
                <h2>Метод сульфата церия &laquo;да-нет&raquo;</h2>
                <p>Метод разработан для применения в медицинской практике, для контроля эффективности стерилизующих/дезинфицирующих растворов, минимальная эффективная концентрация (MEC) которых составляет 6,0 &plusmn;1,0%. Небольшое количество образца добавляют в ампулу с винтовой крышкой, содержащей сульфат церия и индикатор конечной точки &mdash; ферроин. Происходит немедленное изменение цвета, что сигнализирует о том, что концентрация перекиси водорода в образце выше или ниже 6,0%.</p>
