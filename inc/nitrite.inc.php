                <p>Нитриты являются промежуточной формой в азотном цикле, они образуются в процессе разложения органических веществ, но легко окисляются до нитратов. Эти процессы протекают на очистных сооружениях, в системах водоснабжения и природных водах. Нитриты используются в качестве ингибиторов коррозии, консервантов, красителей и в производстве многих органических консервантов. Максимальный уровень загрязнения &mdash; 1 мг/л был установлен USEPA для азота нитритного в питьевой воде.</p>
