            <h2>Метод азокрасителей</h2>
            <p>Нитрит диазотируется с первичным ароматическим амином в кислой среде с образованием сильно окрашенных азокрасителей. Интенсивность окрашивания прямопропорциональна концентрации нитрита в образце. Нитраты не оказывают «помех». Результаты выражаются в ppm (мг/л) NO<sub>2</sub>-N</p>
            <h2>Метод титрования сульфатомцерия</h2>            
            <p>Сульфат церия &mdash; титрант и ферроин &mdash; индикатор конечной точки титрования. На метод не оказывает воздействие гликоль в образцах, содержащих до 75% гликоля, что делает его особенно применимым в системах, содержащих нитриты &mdash; ингибиторы коррозии. Результаты выражаются в ppm (мг/л) NaNO<sub>2</sub>.</p>
