            <h2>Метод изоникотиновая-барбитуровая кислота</h2>
            <p>В тест-наборах <a href="methods.php#chemets">CHEMets</a> и <a href="methods.php#vacu-vials">Vacu-vials</a> хлор добавляют к образцу, который был буферрирован до рН=6. В результате хлорциан реагирует с изоникотиновой и барбитуровой кислотой с образованием синего окрашивания. Результаты выражаются в ppm (мг/л) CN.</p>
            <p>Этот метод имеет два преимущества по сравнению с часто используемыми пиридин методами: срок хранения реагента продлен, лаборант не подвергается воздействию вредных и опасных паров пиридина.</p>
            <h2>Метод нитрата серебра</h2>
            <p>Тест-набор Titrets для определения цианида использует нитрат серебра в качестве титранта и 5-(диметиламиноьензилиден) роданин в качестве индикатора. Изменение окраски с оранжевого на желтый, говорит об окончании титрования. Результаты выражаются в ppm (мг/л) CN.</p>
