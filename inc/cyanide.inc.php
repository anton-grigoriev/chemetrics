                <p>Цианид используется во многих химических и технологических процессах. Он присутствует в сточных водах гальванического производства и процессах очистки металла, коксовых печах, объектах стального производства и газовых скрубберах. Хотя цианид можно удалить щелочным хлорированием, его токсическое воздействие на водные организмы требует постоянного мониторинга сточных вод. Максимальный уровень свободного цианида в питьевой воде составляет 0,2 мг/л.</p>
