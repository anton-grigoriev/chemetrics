            <h2>Метод 4-Аминоантипирин</h2>
            <p>Тест-набор CHEMetrics для определения фенолов использует общепризнанный 4-аминоантипирин (4-ААР) метод. Фенольные соединения вступают в реакцию с 4-ААР в щелочной среде в присутствии феррицианида, в результате формируется красный продукт реакции. Фенол, мета- и орто-замещенные фенолы, и некоторые пара-замещенные фенолы, при соответствующем рН, определяются с помощью этого метода. Метод применим для контроля фенольных соединений в сточных водах. Результаты выражаются в ppm (мг/л) фенола.</p>
