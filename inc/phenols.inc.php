                <p>Фенол (гидроксибензол) является одним из наиболее простых в группе аналогичных органических веществ, которая включает крезолы, ксиленолы и катехолы. Сам фенол является общим компонентом дезинфицирующих средств. В питьевой воде небольшие концентрации фенола придают неприятный вкус и запах, особенно после хлорирования. Высокие концентрации фенола могут указывать на загрязнения промышленных сточных вод или сброс отходов.</p>
