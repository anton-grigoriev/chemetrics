                <p>На качество природной воды указывает уровень растворенного кислорода, водные растения выделяют кислород, а микроорганизмы обычно потребляют его, в то время как они питаются загрязняющими веществами. При низких температурах растворимость кислорода увеличивается; в течение лета, насыщенность уровня может быть до 4 ppm. Растворенный кислород необходим для поддержания жизнедеятельности рыб и других водных организмов, и способствует естественному разложению органических веществ. Очистные сооружения, которые используют аэробный гидролиз, должны поддерживать уровень растворенного кислорода, по крайней мере, 2 ppm.</p>
                <p>При повышенных температурах, кислород оказывает сильное коррозионное воздействие на металлы, что приводит к точечной коррозии металлических систем, таких как котлы высокого давления и оборудование для добычи нефти. Чтобы предотвратить коррозию, жидкости, контактирующие с металлическими поверхностями, должны быть подвергнуты, как правило, физическому и химическому воздействию. Деаэрации может снизить концентрацию растворенного кислорода из питательной воды котла от нескольких ppm до нескольких ppb. Химические восстановители, такие как гидразин, ДЭГА, или сульфит натрия, могут быть использованы вместо/или в сочетании с деаэрацией.</p>
