            <h2>Метод индиго кармин</h2>
            <p>Для контроля окружающей среды и питьевой воды (ppm) используют тест-наборы, использующие метод индиго кармин. Восстановленная форма индиго кармина реагирует с растворенным кислородом, формируется синий комплекс. На этот метод не оказывают влияние: температура, соленость, растворенные газы, такие как сульфиды, которые влияют на анализаторы растворенного кислорода. Результаты выражаются в ppm (мг/л) O<sub>2</sub>.</p>
            <h2>Метод Rhodazine В</h2>            
            <p>Для котловой воды требуется отслеживать уровень растворенного кислорода (ppm), для этого используют тест-наборы, метод Rhodazine D. Разработанный CHEMetrics, Inc, и утвержденный ASTM в качестве опорного метода для определение растворенного кислорода (ppb), Rhodazine D в восстановленной форме реагирует с растворенным кислородом, образуя ярко-розовое окрашивание. На метод не оказывают влияние соленость и растворенные газы. Окислители, в том числе бензохинон, могут привести к высоким результатам. Восстановители, такие как гидразин и сульфит не оказывают влияния. Результаты выражаются в ppm (мг/л) или ppb (мкг/л) O<sub>2</sub>.</p>
            <p>Тест-наборы на низкие диапазоны растворенного кислорода включают специальную пробирку для анализа воды, подаваемой в котлы. Это устройство позволяет пользователю сломать кончик ампулы в текущем потоке образца для того, чтобы исключить влияние атмосферного кислорода.</p>
