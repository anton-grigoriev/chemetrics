            <h2>Общая щелочность</h2>            
            <p>Тест-набор CHEMetrics для определения щёлочности использует титрант&nbsp;&mdash; соляную кислоту и бромокрезол зеленый/метилкрасный индикатор. Конечная точка титрования достигается при рН=4,5. Результаты выражаются в ppm (мг/л) CaCO<sub>3</sub>.</p>            
            <p>Несмотря на то, что тест-набор содержит 30 ампул, свежие контрольные ампулы должны быть приготовлены к каждой серии тестов, поэтому количество образцов, которое может быть измерено, варьируется от 29 (максимум) до 15 (минимум).</p>            
