                <p>Щелочность воды&nbsp;&mdash; это её буферная ёмкость. Щелочность природных вод, как правило, это смесь бикарбоната натрия, карбоната и гидроксид-ионов. Канализационные и сточные воды обычно имеют более высокую щелочность в связи с наличием силикатов и фосфатов.</p>
                <p>Щелочность снижает коррозию в котловой и охлаждающей воде. Она также измеряется при контроле воды и в процессе очистки сточных вод или качества различных отработанных вод.</p>
