            <h2>Гидратная щелочность</h2>
            <p>Гидратная щелочность является одним из компонентов общей щелочности. Операторы котловой воды должны поддерживать относительно высокий уровень гидратной щелочности при воздействии фосфатов, которые используются для смягчения воды и более легкого удаления накипи. Этот специфический тест-набор для гидратной щелочности обеспечивает более точные значения, чем метод расчета.</p>
            <p>Для гидратной щелочности CHEMetrics разработал титриметрический метод, который использует соляную кислоту в качестве титранта, индикатор фенолфталеин и ингибитор. Конечная точка титрования достигается при рН=8,3. Хлорид бария добавляют в образец для снижения помех от карбонатной и бикарбонатной щелочности. Результаты выражаются в ppm (мг/л) NaOH.</p>
