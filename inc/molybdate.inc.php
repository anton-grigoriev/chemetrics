            <article>
                <p>Молибдат используется в качестве ингибитора коррозии в открытых и замкнутых системах водяного охлаждения в промышленных сточных водах и в энергетической промышленности. В растворе анионы молибдата образуют комплекс с окисленным железом, образую защитную пленку молибдата и оксида железа. Молибдат считается эффективной, экологически приемлемой альтернативой хроматировавнию. В отличие от других переходных элементов, молибдат обладает низкой или незначительной токсичностью.</p>
            </article>
            <h2>Метод катехол</h2>
            <p>Метод определения молибдата работает на основе реакции с катехолом. В щелочной среде катехол вступает в реакцию с шестивалентным молибденом, в результате формируется желто-оранжевый хелатный комплекс, который прямопропорцирнален концентрации шестивалентного молибдена. Результаты выражаются в ppm (мг/л) молибдена (Мо).</p>
