                <p>Фосфор обычно встречается в природе в виде фосфата в пластах горных пород земли. Высокие концентрации фосфата могут указывать на сброс удобрений, сброс бытовых сточных вод, или присутствие промышленных отходов или моющих средств. Хотя фосфаты из этих источников обычно поли-фосфаты или органически связанные, со временем все они будут разлагаться до орто- или реакционно-способных фосфатов.</p>
                <p>Измерение фосфатов используется для контроля накипи и уровня ингибиторов коррозии в котлах и охлаждающих камерах. Оба метода, описанных ниже, измеряют реакционно-способные фосфаты, которые дадут положительный отклик перед началом гидролиза, и обычно называются орто-фосфатами.</p>
