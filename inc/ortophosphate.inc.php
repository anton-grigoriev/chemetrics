            <h2>Метод Ванадиймолибдофосфорная кислота</h2>
            <p>В тест-наборе CHEMetrics используется метод ванадиймолибдофосфорной ксилоты, фосфат вступает в реакцию с молибдатом аммония в кислой среде и в присутствии ванадия, в результате образуется комплекс желтого цвета. Результаты выражаются в ppm (мг/л) PO<sub>4</sub>.</p>
            <h2>Метод хлорида олова</h2>
            <p>В тест-наборе CHEMetrics используется метод восстановления хлорида олова. Фосфат вступает в реакцию с молибдатом аммония и затем восстанавливает хлорид олова, в результате формируется синий комплекс. Результаты выражаются в ppm (мг/л) PO<sub>4</sub>.</p>
