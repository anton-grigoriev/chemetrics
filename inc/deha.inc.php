            <h2>Метод PDTS</h2>
            <p>Тест-набор использует PDTS метод, в котором ДЭГА восстанавливает железо III до железа II, которое легко вступает в реакцию с PDTS (3-(2-пиридил)-5,6-бис(4-фенилсульфоновая кислота)-1,2,4-триазин двунатриевая соль), в результате образуется комплекс розово-лилового цвета, который прямопропорционален концентрации ДЭГА. Результаты выражаются в ppb (μг/л) или ppm (мг/л) ДЭГА.</p>
            <h2>Сульфат церия титриметрический метод</h2>
            <p>CHEMetrics разработал титриметрический метод, который использует титрант &mdash; сульфат церия и ферроин &mdash; индикатор конечной точки. ДЭГА восстанавливает трехвалентное железо до двухвалентного, и в результате двухвалентное железо титруют сульфатов церия. Результаты выражаются в ppm (мг/л) ДЭГА.</p>
