            <article>
                <p>Растворенный кислород в системах котловой воды вызывает коррозию и разъедание металлической поверхности, что может привести к порче котла, к поломке оборудования и простою системы. ДЭГА (N,N-диэтилгидроксиламин) добавляют в систему котловой воды, в качестве поглотителя кислорода для поддержания как можно более низкого уровня растворенного кислорода.</p>              
