            <article>
                <p>Определение химического потребления кислорода (ХПК) широко используется в муниципальных и промышленных лабораториях для измерения общего уровня органических загрязнений в сточных водах. Уровень загрязнения определяется путем измерения эквивалентного количества кислорода, необходимого для окисления органических веществ в образце.</p>
