            <h2>Метод разложения дихромата в термореакторе</h2>
            <p>CHEMetrics предлагает два метода разложения дихромата в термореакторе для быстрого, легкого и безопасного определения низкого, среднего и высокого уровня ХПК в сточных водах: метод, утвержденный USEPA, и метод, не содержащий ртуть. Продукция, использующая метод, утвержденный USEPA, содержит в составе сульфат ртути для устранения «помех» от хлоридов. Лучше использовать наборы, не содержащие ртуть, тогда «помехи» от хлоридов можно принять не существенными и не требуется отчетности USEPA</p>
            <p>Герметичные ампулы CHEMetrics содержат предварительно отмеренные растворы серной кислоты и дихромата калия. Для определения ХПК лаборант просто снимает покрытую тефлоном винтовую крышку с ампулы, добавляет образец в ампулу и закрывает крышкой. Затем ампулу нагревают в течение двух часов при 150&deg;С в стандартном термореакторе. После завершения разложения пробы в термореакторе результаты могут быть получены с помощью любого спектрофотометра, способного принимать 16 мм ампулы. ХПК ампулы CHEMetrics могут быть использованы непосредственно с мультипараметрическим <a href="/tools.php#v2k">фотометром CHEMetrics V-2000</a>, <a href="/tools.php#sam">монопараметрическим фотометром</a> ХПК CHEMetrics, а также со спектрофотометрами Hach. Встроенные ХПК методы Hach и калибровки могут быть использованы без новой калибровки. Общие таблицы калибровки для использования с другими спектрофотометрами входят в комплект поставки тест-наборов CHEMetrics.</p>
            <p>Все тест-наборы для ХПК необходимо использовать с термореактором для разложения проб и с <a href="/tools.php#v2k">фотометром V-2000</a>, <a href="/tools.php#sam">монопараметрическим фотометром ХПК</a> или спектрофотометром, способным принимать круглые 16 мм кюветы. Приборы продаются отдельно.</p>
            <p>Свежие контрольные ампулы должны быть приготовлены к каждой серии тестов, поэтому количество образцов, которое может быть измерено, варьируется.</p>
