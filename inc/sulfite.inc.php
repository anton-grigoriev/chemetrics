            <h2>Метод йодометрический (Сульфит)</h2>
            <p>Тест-набор CHEMetrics для определения сульфита использует йодометрический метод, в котором сульфит титруют йодид-йодатом (титрант) в кислой среде, с применением крахмального индикатора. Тиосульфат титруют как сульфит. Результаты выражаются в ppm (мг/л) SO<sub>3</sub>.</p>
            <h2>Метод Ripper (Сульфиты в вине)</h2>
            <p>Тест-набор CHEMetrics для определения сульфита использует метод Ripper, который использовали в винодельческой промышленности в течение многих лет в качестве стандарта для быстрого анализа сульфитов.</p>
            <p>Сульфиты титруют раствором йодид-йодата, используя крахмал в качестве индикатора конечной точки титрования. Фосфорная кислота используется для регулирования рН образца. Результаты выраженные количественно считываются прямо с ампулы. Тест определяет свободные сульфиты в виде ppm (мг/л) SO<sub>2</sub>. Результаты этого тест-набора приемлемы для сухих белых вин (хотя они могут иметь погрешность до 10 ppm). Данный тест-набор не рекомендован для использования с красными или белыми винами, содержащие аскорбиновую кислоту или таннин. Эти вина часто дают ложно высокие результаты теста.</p>
