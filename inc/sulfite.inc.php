                <p>Сульфиты обычно не присутствую в поверхностных водах. Если сульфиты сбрасываются в сточные воды или из бытовых сточных вод, они сразу же окисляются до сульфатов. Сульфит натрия – наиболее распространенная форма сульфитов, он является отличным восстановителем и используется в качестве поглотителя кислорода. Концентрация сульфита в котловой и технической воде должна постоянно контролироваться, чтобы избежать избыточного использования деэмульгатора. Очистные сооружения, которые используют диоксид серы для удаления избытков хлора, должны контролировать в своих стоках сульфиты.</p>
                <p>Сульфиты использовались веками для дезинфекции и сохранения продуктов. Они используются во всем мире в винодельческой промышленности в качестве антиоксиданта и противомикробных агентов. Однако, сульфиты были признаны возбудителем некоторых аллергических реакций, перенесенных астматиками. В результате, FDA (Управление по санитарному надзору за пищевыми продуктами и медикаментами) и Бюро Алкоголя, табака и огнестрельного оружия, обязали указывать на этикетке концентрацию сульфита, если он содержится в продуктах питания и напитках от 10 ppm и выше.</p>
